namespace Android
{
/// <summary>
/// Method names for Android objects.
/// </summary>
public static class AndroidMethodNames
{
    /// <summary>
    /// Method name for starting an activity.
    /// </summary>
    public const string StartActivity = "startActivity";
    
    /// <summary>
    /// Method name for getting a content resolver.
    /// </summary>
    public const string GetContentResolver = "getContentResolver";
    
    /// <summary>
    /// Method name for getting an integer from the static global settings object.
    /// </summary>
    public const string GetInt = "getInt";

    /// <summary>
    /// Method name for getting a string from the static secure settings object.
    /// </summary>
    public const string GetString = "getString";

}
}
