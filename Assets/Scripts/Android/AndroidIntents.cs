namespace Android
{
/// <summary>
/// Definitions of Android intent names.
/// </summary>
public static class AndroidIntents
{
    /// <summary>
    /// Intent name for opening the Android settings.
    /// </summary>
    public const string AndroidSettings = "android.settings.SETTINGS";
    
    /// <summary>
    /// Intent name for opening the bluetooth settings.
    /// </summary>
    public const string BluetoothSettings = "android.settings.BLUETOOTH_SETTINGS";

    /// <summary>
    /// Intent name for opening the developer options.
    /// </summary>
    public const string DeveloperOptions = "android.settings.APPLICATION_DEVELOPMENT_SETTINGS";
    
    /// <summary>
    /// Intent name for opening the device information settings.
    /// </summary>
    public const string DeviceInfoSettings = "android.settings.DEVICE_INFO_SETTINGS";
    
    /// <summary>
    /// Intent name for opening the application settings.
    /// </summary>
    public const string ManageAppSettings = "android.settings.MANAGE_APPLICATIONS_SETTINGS";
}
}
